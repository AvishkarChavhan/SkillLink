const convertUserDataTOPDF = (userData) => {
    return new Promise((resolve, reject) => {
        const doc = new PDFdocument({ margin: 50 });
        const filename = crypto.randomBytes(16).toString("hex") + ".pdf";
        const uploadDir = path.join(process.cwd(), "uploads");

        if (!fs.existsSync(uploadDir)) {
            fs.mkdirSync(uploadDir);
        }

        const outputPath = path.join(uploadDir, filename);
        const stream = fs.createWriteStream(outputPath);
        doc.pipe(stream);

        // ✅ Header Section
        doc.fontSize(24).font("Helvetica-Bold").text(userData.userId.name, { align: "center" });
        doc.moveDown(0.3);
        doc.fontSize(12).font("Helvetica").fillColor("grey").text(`@${userData.userId.username}`, { align: "center" });
        doc.moveDown(0.3);
        doc.fontSize(12).fillColor("grey").text(userData.userId.email, { align: "center" });
        doc.moveDown(1);

        // ✅ Divider
        doc.moveTo(50, doc.y).lineTo(550, doc.y).strokeColor("#cccccc").stroke();
        doc.moveDown(1);

        // ✅ Bio Section
        if (userData.bio) {
            doc.fontSize(14).font("Helvetica-Bold").fillColor("black").text("About", { align: "left" });
            doc.moveDown(0.4);
            doc.fontSize(11).font("Helvetica").fillColor("#333333").text(userData.bio, { align: "left" });
            doc.moveDown(1);

            doc.moveTo(50, doc.y).lineTo(550, doc.y).strokeColor("#cccccc").stroke();
            doc.moveDown(1);
        }

        // ✅ Current Position Section
        if (userData.currentPost) {
            doc.fontSize(14).font("Helvetica-Bold").fillColor("black").text("Current Position", { align: "left" });
            doc.moveDown(0.4);
            doc.fontSize(11).font("Helvetica").fillColor("#333333").text(userData.currentPost, { align: "left" });
            doc.moveDown(1);

            doc.moveTo(50, doc.y).lineTo(550, doc.y).strokeColor("#cccccc").stroke();
            doc.moveDown(1);
        }

        // ✅ Work History Section
        if (userData.pastWork && userData.pastWork.length > 0) {
            doc.fontSize(14).font("Helvetica-Bold").fillColor("black").text("Work History", { align: "left" });
            doc.moveDown(0.6);

            userData.pastWork.forEach((work, index) => {
                doc.fontSize(12).font("Helvetica-Bold").fillColor("#222222").text(`${work.company}`, { align: "left" });
                doc.fontSize(11).font("Helvetica").fillColor("#555555").text(`Position: ${work.position}`, { align: "left" });
                if (work.years) {
                    doc.fontSize(11).fillColor("#888888").text(`Years: ${work.years}`, { align: "left" });
                }
                if (index < userData.pastWork.length - 1) {
                    doc.moveDown(0.5);
                    doc.moveTo(50, doc.y).lineTo(550, doc.y).strokeColor("#eeeeee").stroke();
                }
                doc.moveDown(0.8);
            });
        }

        // ✅ Footer
        doc.moveTo(50, doc.y).lineTo(550, doc.y).strokeColor("#cccccc").stroke();
        doc.moveDown(0.5);
        doc.fontSize(9).fillColor("grey").text("Generated by Frindora", { align: "center" });

        doc.end();

        stream.on("finish", () => { resolve(filename); });
        stream.on("error", (err) => { reject(err); });
    });
};

export const register = async (req, res) => {
    try {
        const { name, email, password, username } = req.body;
        if (!name || !email || !password || !username)
            return res.status(400).json({ message: "All fields are required" });
        const user = await User.findOne({ email });
        if (user) return res.status(400).json({ message: "user already exists" });
        const hashedPassword = await bcrypt.hash(password, 10);
        const newUser = new User({
            name,
            email,
            password: hashedPassword,
            username,
        });
        await newUser.save();
        const profile = new Profile({ userId: newUser._id });
        await profile.save();
        return res.json({ message: "user created" });
    } catch (err) {
        return res.status(500).json({ message: err.message });
    }
};

export const Login = async (req, res) => {
    try {
        const { email, password } = req.body;
        if (!email || !password)
            return res.status(400).json({ message: "all fields are required" });
        const user = await User.findOne({ email });
        if (!user)
            return res.status(404).json({ message: "User does not exist" });
        const isMatch = await bcrypt.compare(password, user.password);
        if (!isMatch)
            return res.status(400).json({ message: "Invalid Credentials" });
        const newtoken = crypto.randomBytes(32).toString("hex");
        user.token = newtoken;
        await user.save();
        return res.json({ token: newtoken });
    } catch (err) {
        return res.status(500).json({ message: err.message });
    }
};

